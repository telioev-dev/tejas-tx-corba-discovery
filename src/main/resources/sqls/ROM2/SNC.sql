CREATE INDEX "idx_TEJASNETWORKS_SNC_ROM 2_circuit_id" ON "TEJASNETWORKS_SNC_ROM 2" (circuit_id);
CREATE INDEX "idx_TEJASNETWORKS_SNC_ROM 2_srf_id" ON "TEJASNETWORKS_SNC_ROM 2" (srf_id);
